#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (*lst == NULL)
	{
		*lst = new;
	}
	if (new == NULL)
		return ;
	last = ft_lstlast(*lst);
	last->next = new;
	new->next = NULL;
}
